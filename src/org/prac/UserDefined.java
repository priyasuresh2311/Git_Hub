package org.prac;

public class UserDefined {
private int idNo;
private long PhNo;
private String name;

public int getIdNo() {
	return idNo;
}
public long getPhNo() {
	return PhNo;
}
public String getName() {
	return name;
}
public void setIdNo(int idNo) {
	this.idNo = idNo;
}
public void setPhNo(long phNo) {
	PhNo = phNo;
}
public void setName(String name) {
	this.name = name;
}

}
